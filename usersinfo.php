					if ($this->level->Exportable) $Doc->ExportCaption($this->level);
					if ($this->_email->Exportable) $Doc->ExportCaption($this->_email);
					if ($this->activated->Exportable) $Doc->ExportCaption($this->activated);
					if ($this->profile->Exportable) $Doc->ExportCaption($this->profile);
				} else {
					if ($this->user_id->Exportable) $Doc->ExportCaption($this->user_id);
					if ($this->password->Exportable) $Doc->ExportCaption($this->password);
					if ($this->level->Exportable) $Doc->ExportCaption($this->level);
					if ($this->_email->Exportable) $Doc->ExportCaption($this->_email);
					if ($this->activated->Exportable) $Doc->ExportCaption($this->activated);
				}
				$Doc->EndExportRow();
			}
		}

		// Move to first record
		$RecCnt = $StartRec - 1;
		if (!$Recordset->EOF) {
			$Recordset->MoveFirst();
			if ($StartRec > 1)
				$Recordset->Move($StartRec - 1);
		}
		while (!$Recordset->EOF && $RecCnt < $StopRec) {
			$RecCnt++;
			if (intval($RecCnt) >= intval($StartRec)) {
				$RowCnt = intval($RecCnt) - intval($StartRec) + 1;

				// Page break
				if ($this->ExportPageBreakCount > 0) {
					if ($RowCnt > 1 && ($RowCnt - 1) % $this->ExportPageBreakCount == 0)
						$Doc->ExportPageBreak();
				}
				$this->LoadListRowValues($Recordset);

				// Render row
				$this->RowType = EW_ROWTYPE_VIEW; // Render view
				$this->ResetAttrs();
				$this->RenderListRow();
				if (!$Doc->ExportCustom) {
					$Doc->BeginExportRow($RowCnt); // Allow CSS styles if enabled
					if ($ExportPageType == "view") {
						if ($this->user_id->Exportable) $Doc->ExportField($this->user_id);
						if ($this->password->Exportable) $Doc->ExportField($this->password);
						if ($this->level->Exportable) $Doc->ExportField($this->level);
						if ($this->_email->Exportable) $Doc->ExportField($this->_email);
						if ($this->activated->Exportable) $Doc->ExportField($this->activated);
						if ($this->profile->Exportable) $Doc->ExportField($this->profile);
					} else {
						if ($this->user_id->Exportable) $Doc->ExportField($this->user_id);
						if ($this->password->Exportable) $Doc->ExportField($this->password);
						if ($this->level->Exportable) $Doc->ExportField($this->level);
						if ($this->_email->Exportable) $Doc->ExportField($this->_email);
						if ($this->activated->Exportable) $Doc->ExportField($this->activated);
					}
					$Doc->EndExportRow();
				}
			}

			// Call Row Export server event
			if ($Doc->ExportCustom)
				$this->Row_Export($Recordset->fields);
			$Recordset->MoveNext();
		}
		if (!$Doc->ExportCustom) {
			$Doc->ExportTableFooter();
		}
	}

